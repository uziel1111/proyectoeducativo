            <h4 class="section-heading text-uppercase text-left"><span>INSCRIPCIONES</span></h4>
            <p>
            Los sistemas de inscripciones no solo distribuyen estudiantes en las distintas escuelas; pueden también ser mecanismos de apoyo a los padres en el proceso de acompañamiento de sus hijos; generadores de información de la mayor utilidad para planear los servicios educativos de la mejor manera; y herramientas para aportar certidumbre y transparencia de estos procesos.
                </p>
                <p>
                En 2016 diseñamos, en coordinación con la Secretaría de Educación y Cultura de Sonora, un proceso para organizar la inscripción de los alumnos que acceden al primer grado de preescolar, primaria y secundaria. Con el apoyo de un sistema eficaz, los padres de familia por primera vez pudieron llevar a cabo en línea el registro de opciones y la posterior recepción de resultados en forma ordenada y eficiente. Este sistema atiende a más de 165 mil personas y fue considerado como uno de los avances más importantes de 2019 en educación por parte del Gobierno del Estado.
