          <h4 class="section-heading text-uppercase text-left"><span>SISTEMAS DE CONSULTA A ESCUELAS Y DOCENTES</span></h4>
          <p>
         <strong>Los sistemas de información estatales también son herramientas muy valiosas para recoger la percepción de docentes y directores escolares sobre diversos temas</strong>. Hemos desarrollado esquemas de consulta para levantar encuestas estatales con alcance censal y a un costo mínimo. Por ejemplo, <strong>el Estado de Sonora diseñó en 2016 una estrategia para atender las necesidades más elementales de las escuelas en materia de infraestructura</strong>. Para ello requería información de las necesidades que en este sentido tenía cada una de las escuelas públicas del estado. Se diseñó e implementó entonces una encuesta electrónica para los directores de todos los planteles de educación básica de ese estado, por vía del sistema de control escolar. De esa manera fue posible conocer, organizar y priorizar la atención de las necesidades de agua potable, electricidad, mesabancos y aire acondicionado (indispensable en muchas zonas de Sonora), así como de problemas en la distribución de libros de texto, identificación de grupos sin docente y denuncias de robo en el periodo vacacional. 
          </p>
          <p>
          En los estados de Coahuila y Puebla también se han levantado encuestas por esa vía para conocer la percepción docente en temas específicos de interés para la autoridad educativa. De esta manera, <strong>se multiplican las vías de comunicación entre el magisterio y la autoridad para que la toma de decisiones cuente con el mejor sustento posible.</strong>
          </p>
