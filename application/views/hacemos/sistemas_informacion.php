<h4 class="section-heading text-uppercase text-left"><span>HACIA UN SISTEMA DE INTELIGENCIA INFORMATIVA</span></h4>
            <p>
            La información en el sistema educativo puede ser cimiento y brújula para la mejora. El sector educativo se caracteriza por subutilizar la información que tiene disponible. Un porcentaje elevadísimo de los datos que se recogen de todos los alumnos, padres de familia y escuelas del país no encuentra ninguna aplicación práctica. Nos hemos dado a la tarea de aprovechar todo ese potencial para fortalecer la toma de decisiones académicas, la prevención del abandono escolar y la descarga administrativa, sin que ello implique trabajo de captura adicional para ninguna de las partes. <strong> Hemos logrado construir sistemas para colectar la información de manera más simple e intuitiva; para integrar los datos recopilados por distintas instancias dentro y fuera del sector educativo; para analizarlos de manera que puedan ser útiles a usuarios concretos (docentes, alumnos, padres de familia, directores escolares, supervisores, funcionarios, sociedad en general); y para distribuirlos a esos usuarios en un lenguaje, un formato y por un medio que se ajuste a sus posibilidades y necesidades</strong>.
                </p>
                <p>
                Actualmente, los estados de Puebla y Sonora cuentan con avanzados sistemas de información que han sido parte de importantes procesos de mejora en sus resultados educativos. El más reciente, Yolixtli, comenzó a operar en 2019. Su nombre hace referencia a los términos <i>“yólotl”</i> e <i>“ixtli”</i> (“corazón” y “rostro” en lengua náhuatl), que están asociados al gran objetivo de la educación entre los aztecas, que según Miguel León Portilla era “hacer corazones firmes y rostros sabios”.
                </p>
                <p>
