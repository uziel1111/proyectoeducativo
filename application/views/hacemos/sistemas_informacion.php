            <h4 class="section-heading text-uppercase text-left"><span>REPORTE APA (ASISTENCIA, PERMANENCIA, APRENDIZAJE)</span></h4>
            <p>
           <strong> En 2016 diseñamos el Reporte APA, una herramienta para apoyar los esfuerzos de prevención contra el abandono escolar</strong> que, sin solicitar información adicional a las escuelas, identifica con claridad a los estudiantes que se han dado de baja del sistema educativo estatal en cada una de las primarias y secundarias del estado, así como a aquellos que están en alto riesgo de abandonar sus estudios, de acuerdo con el análisis de sus niveles de inasistencia, sus calificaciones y su condición de extraedad. 
                </p>
                <p>
                Un aspecto esencial de la eficacia de este documento es que <strong>se alimenta exclusivamente de la información que ya contienen los sistemas de control escolar</strong>, por lo que no genera carga alguna a las escuelas. Así mismo, su distribución es sumamente eficaz y no implica costos porque se realiza por la vía de los propios sistemas de control escolar, de manera que <strong>cada escuela recibe su reporte personalizado de manera bimestral</strong> a efecto de que puedan utilizarlo como insumo en las reuniones de Consejo Técnico. 
                </p>
                <p>
                Reportes paralelos con la información agregada por municipio y a nivel estatal fueron distribuidos también a las autoridades municipales y al Gobernador del Estado. <strong>El Reporte APA obtuvo el Primer lugar en la categoría académica en el Reconocimiento de la Mejora de la Gestión 2016, un Premio que la SEP Federal otorga anualmente a buenas prácticas de todo el país</strong>.  
                </p>                
