<p>
  Todos los esfuerzos de este programa apuntaron a <strong>3er grado de Preescolar, 1er y 2do grados de Primaria y 1er grado de Secundaria</strong>, pues se trataba de ayudar a los estudiantes desde el inicio del nivel respectivo. 
  </p>
  <p>
  El PAI tuvo logros notables: entre 2013 y 2015, <strong>las 200 secundarias focalizadas dejaron de estar en el último cuartil de logro, para alcanzar y superar la media nacional tanto en Lenguaje y comunicación como en Matemáticas</strong> en la prueba PLANEA. Ello tuvo un impacto muy importante en el conjunto de escuelas de ese nivel: <strong>en ese año Puebla alcanzó, por primera vez en su historia, el Primer lugar nacional de Secundaria en Matemáticas, así como el 3er lugar en Lenguaje y comunicación</strong>. El estado avanzó significativamente en PLANEA a partir de una intervención enfocada en las escuelas de más bajo rendimiento: <strong>la equidad fue un camino muy eficaz para elevar la calidad</strong>.       
