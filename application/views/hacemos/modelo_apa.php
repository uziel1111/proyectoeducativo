                <h3>Modelo APA</h3>
                <p>
               <strong> El Modelo de Asistencia, Permanencia y Aprendizaje (APA)</strong> es una contribución de ProyEdu a la gestión educativa mexicana. Ha sido adoptado en los estados de Coahuila, Puebla y Sonora. <strong>Consiste en mantener a esos tres objetivos esenciales de la educación como referencia permanente en la toma de decisiones de todas las instancias del sistema</strong>. La intención es procurar una mayor convergencia de acciones y recursos en torno a los aspectos fundamentales del quehacer educativo. 
                </p>
                <p>
                Este Modelo, diseñado entre 2013 y 2014, adapta la lógica de otro puesto en marcha en la provincia de Ontario, Canadá. En ese modelo se proponían también tres objetivos: que todos los estudiantes concluyeran el 12º grado (high school), además de haber aprendido lo esencial tanto del Inglés como de las Matemáticas. Esa propuesta de política educativa, que luego evolucionó conforme los objetivos se cumplían, hizo que Ontario se consolidara como potencia educativa mundial. <strong>En el Estado de Puebla, México, el Modelo APA contribuyó a que ese estado, quinto con mayor porcentaje de pobreza en un país con 32 estados, pasara del lugar 23 en las pruebas estandarizadas nacionales de la educación media superior (Grado 12) en 2010, al primer lugar nacional en 2015, además de avanzar en cobertura educativa y permanencia escolar.</strong>       
                </p>                                                                                                                                                                    

