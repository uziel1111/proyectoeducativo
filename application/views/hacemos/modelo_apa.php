  <h4>Programa de Atención Intensiva (PAI) en Puebla</h4>
  <p>
  Se trata de una intervención a 500 escuelas de educación básica en la que participamos desde el diseño del programa y la gestión de los fondos, hasta la implementación y el acompañamiento. En 2013, la Secretaría de Educación Pública sometió a concurso fondos para impulsar la calidad educativa en los estados. Proyecto Educativo SC dirigió la construcción de una propuesta que pudiera obtener financiamiento, con la cual <strong>logramos que Puebla recibiera $30 millones de pesos</strong>, la máxima cantidad que podía obtenerse en ese proceso. Cabe señalar que <strong>nuestra organización nunca ha solicitado ni obtenido comisión alguna por gestionar recursos para la educación pública</strong>. Este presupuesto resultó ser el inicio de importantes acciones educativas para la educación básica en ese Estado.
  </p>
  <p>
  Así nació el Programa de Atención Intensiva (PAI), orientado a reforzar la calidad educativa en las escuelas con mayor rezago académico en el estado. Para asegurar una atención de alto nivel, <strong>se focalizaron 500 escuelas: 200 primarias y 200 secundarias</strong> que se encontraban en el cuartil de desempeño más bajo de ENLACE 2013, <strong>así como 100 preescolares</strong> cuyos egresados eran atendidos mayoritariamente por las primarias focalizadas. El PAI contempló líneas de acción para cada uno de los actores involucrados: estudiantes, supervisores, directores escolares, docentes y padres de familia. Con esta visión se incluyeron actividades como la evaluación de competencias lectoras y de pensamiento matemático para pequeños de 2º grado de Primaria; la organización de cursos de verano para fortalecer las habilidades que el diagnóstico revelara como insuficientes; capacitación de calidad a docentes, directivos y supervisores de todas las escuelas participantes; y guías para trabajar con padres de familia. 
</p>
