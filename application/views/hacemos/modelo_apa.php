  <h4>El Modelo APA incluye objetivos, estrategias e indicadores:</h4>
  <p>
  <strong>Objetivos</strong> 

<ul class="fa-ul">
  <li class="mb-2"><span class="fa-li"><i class="fas fa-check-square fc-1"></i></span><strong>Asistencia.</strong> Que todas las niñas y niños de entre 3 y 17 años asistan a la escuela.</li>
  <li class="mb-2"><span class="fa-li"><i class="fas fa-check-square fc-1"></i></span><strong>Permanencia.</strong> Que todas y todos los estudiantes concluyan la educación media (12º).</li>
  <li class="mb-2"><span class="fa-li"><i class="fas fa-check-square fc-1"></i></span><strong>Aprendizaje.</strong> Que cada alumna y alumno adquieran al menos los conocimientos básicos de los planes y programas de estudio.</li>
</ul>
	

  </p>

  <strong>Estrategias</strong>  

<ul class="fa-ul">
  <li class="mb-2"><span class="fa-li"><i class="fas fa-check-square fc-1"></i></span><strong>Focalización.</strong>Todas las acciones deben focalizarse, ante los insuficientes recursos disponibles (personas, fondos y demás apoyos). Ello para buscar una mayor equidad, que implica intensificar los esfuerzos entre las personas que más los requieren con el propósito de <strong>reducir las brechas</strong>. Es indispensable identificar un universo de atención que asegure el impacto real.</li>
  <li class="mb-2"><span class="fa-li"><i class="fas fa-check-square fc-1"></i></span><strong>Articulación.</strong>Para asegurar la complementariedad de las acciones, se busca lograr una buena coordinación entre programas, sean federales o estatales; entre los distintos <strong>niveles educativos</strong>; y <strong>entre dependencias</strong> de dentro y fuera del sector educativo.</li>
  <li class="mb-2"><span class="fa-li"><i class="fas fa-check-square fc-1"></i></span><strong>Implementación y acompañamiento.</strong>Para ello es necesaria una buena <strong>comunicación</strong> que asegure que todas las personas e instancias involucradas conozcan y compartan las características de los esfuerzos a realizar, además de tener plena claridad de sus atribuciones y responsabilidades. Un acompañamiento adecuado también debe permitir el monitoreo de la evaluación de la estrategia y el sentir de las personas involucradas, a efecto de identificar a tiempo riesgos y oportunidades.</li>
</ul>


  <p>
  <strong>Indicadores</strong>  
  </p>
  <p>
Buena parte del éxito de este modelo radica en la facilidad para entender y compartir sus objetivos, pero también en la posibilidad de seguir pocos pero muy importantes indicadores. Y son pocos porque, entre más prioridades establezcamos en la política educativa, menos prioritaria será cada una de ellas. Los indicadores tienen varias características:
 
  </p>

  <ul class="fa-ul">
  <li class="mb-2"><span class="fa-li"><i class="fas fa-check-square fc-1"></i></span><strong>Cada indicador es una meta en sí mismo</strong>, pues todos se refieren a resultados, más que a insumos o procesos.</li>
  <li class="mb-2"><span class="fa-li"><i class="fas fa-check-square fc-1"></i></span>Apuntan a la <strong>prioridad de lograr un mínimo indispensable para todos</strong>, como expresión clara de equidad.</li>
  <li class="mb-2"><span class="fa-li"><i class="fas fa-check-square fc-1"></i></span>Están formulados en <strong>sentido positivo</strong>: en todos los casos un mayor nivel es mejor.</li>
</ul>    



