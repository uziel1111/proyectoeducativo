<h4>Gestión de recursos</h4>
<p>
Nuestra organización es una instancia de asesoría e investigación aplicada. Como tal, <strong>ProyEdu continuamente genera o participa en propuestas innovadoras para mejorar los procesos del sector educativo</strong>. Esas propuestas, sin embargo, no siempre encuentran todo el respaldo financiero requerido en los gobiernos federal o estatales, por lo que es necesario buscar los apoyos correspondientes. Cabe señalar que nuestra organización nunca ha solicitado ni aceptado comisión alguna por la obtención de recursos para efectuar un proyecto en los estados.
</p>
<p>
Algunos de esos proyectos son:
</p>
  <ul class="fa-ul">
  <li class="mb-2"><span class="fa-li"><i class="fas fa-check-square fc-1"></i></span><strong>Programa de Atención Intensiva (PAI) </strong>de Puebla, para el que obtuvimos $30 millones de pesos de la SEP.</li>
  <li class="mb-2"><span class="fa-li"><i class="fas fa-check-square fc-1"></i></span><strong>Enseñanza y Aprendizaje Innovadores (ITL)</strong> de Microsoft Partners in Learning, en el que coordinamos un fondo de $3 millones de pesos aportados por la SEP.</li>
  <li class="mb-2"><span class="fa-li"><i class="fas fa-check-square fc-1"></i></span><strong>Academia Sonorense de Matemáticas</strong>, que operó con un financiamiento de $2 millones de pesos por parte del Massachusetts Institute of Technology (MIT) y del Conacyt.</li>
  <li class="mb-2"><span class="fa-li"><i class="fas fa-check-square fc-1"></i></span><strong>Estudio de abandono escolar en secundaria y media superior en Puebla</strong>, que requirió atraer $1 millón de pesos del Consejo Estatal de Ciencia y Tecnología, además del Conacyt.</li>
