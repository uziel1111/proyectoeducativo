            <div class="col-md-6 text-justify">
            <p>
                Una acción concreta de combate al abandono escolar en Puebla fue el diseño del <strong>Reporte APA</strong>, que es un documento que <strong>identifica con claridad a los estudiantes que se han dado de baja del sistema educativo estatal en cada una de las primarias y secundarias del estado, así como a aquellos que están en alto riesgo de abandonar sus estudios</strong>, de acuerdo con el análisis de sus niveles de inasistencia, sus calificaciones y su condición de extraedad. Un aspecto esencial de la eficacia de este documento es que <strong>se alimenta exclusivamente de la información que ya contienen los sistemas de control escolar</strong>, por lo que su integración no genera carga adicional de trabajo a las escuelas. Así mismo, su distribución no implica costos porque se realiza por la vía de los propios sistemas de control escolar, de manera que <strong>cada escuela recibe su reporte personalizado de manera bimestral</strong> a efecto de que puedan utilizarlo como insumo en las reuniones de Consejo Técnico. Reportes paralelos con la información agregada por municipio y a nivel estatal fueron distribuidos también a las autoridades municipales y al Gobernador del Estado. <strong>El Reporte APA obtuvo el Primer lugar en la categoría académica en el Reconocimiento de la Mejora de la Gestión 2016, un Premio que la SEP Federal otorga anualmente a buenas prácticas de todo el país</strong>.  
                </p>
