                </p>
                <p>
                Con la elaboración y distribución del Reporte APA a todas las primarias y secundarias, se hizo evidente la necesidad de articular mejor las acciones de instancias estatales, municipales y de la sociedad civil para responder ante las causas del abandono escolar. De ahí que en 2017 <strong>ProyEdu planteara la necesidad de crear una oficina que promoviera la articulación de esfuerzos dentro y fuera del sector educativo para favorecer la permanencia escolar. Así nació la Unidad de Promoción del Derecho a la Educación (UNIPRODE)</strong> en 2017. Esa Unidad contó con la invaluable participación de un grupo de profesionales de la educación que logró que su trabajo fuera palpable en muy corto tiempo. 
