Las acciones realizadas en Puebla han rendido frutos. La inasistencia escolar de personas entre los 3 y los 17 años en el Estado de Puebla pasó de 350 mil en 2010 a 251 mil en 2015. Es decir, <strong>en cinco años el avance en el objetivo de universalizar la asistencia y la permanencia fue de casi cien mil personas</strong>. Estas estrategias, y la creación de la UNIPRODE, lograron además que los sismos de septiembre de 2017 que afectaron a escuelas de 105 municipios del estado, no derivaran en un incremento en el abandono escolar como ha ocurrido en otros eventos de esa naturaleza en el mundo. De hecho, el abandono escolar en los municipios afectados continuó la tendencia a la baja observada en años anteriores, incluso en el nivel de secundaria, lo cual resalta aún más los esfuerzos realizados por la SEP de Puebla ante tal contingencia. 
