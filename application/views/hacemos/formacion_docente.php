Como puede observarse, <strong>la capacitación docente busca sustentarse siempre en equipos técnicos locales, idealmente constituidos en academias estatales, que aporten eficacia y continuidad a estos procesos</strong>. Si bien el diseño de los cursos y los mecanismos de acompañamiento pueden ser externos al sistema educativo estatal, es fundamental que cada entidad construya sus capacidades académicas propias, con personal preferentemente de base, para no depender de instancias externas; ello derivará en mayor pertinencia en los contenidos y costos financieros significativamente menores. Así mismo, dichas academias pueden constituirse en órganos de consulta y discusión para la toma de decisiones, además de representar al estado en todo género de actividades nacionales e internacionales. Todo lo anterior permite que las academias operen como elementos de continuidad para la capacitación docente y, en general, para la gestión educativa en los estados. 
