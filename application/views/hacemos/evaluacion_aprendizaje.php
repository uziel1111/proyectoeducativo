            <h4 class="section-heading text-uppercase text-left"><span>USO DE RESULTADOS DE EVALUACIONES ESTANDARIZADAS</span></h4>
                <p>
               <strong> La utilidad de una evaluación para elevar el aprendizaje está estrechamente vinculada con la manera en que los resultados se difunden, se asimilan y se traducen en acciones concretas</strong> en distintos ámbitos: en el aula, en la escuela, en la familia, en la supervisión o bien en las esferas estatales y federales. Con el inicio de la prueba PISA en el año 2000, y posteriormente con el inicio de las evaluaciones estandarizadas del aprendizaje en México (ENLACE, 2006), comenzamos a realizar amplios análisis de sus resultados y, posteriormente, ofrecimos talleres a docentes, directores escolares, funcionarios y periodistas para ayudar a comprender el propósito de esas evaluaciones, sus características y el significado de sus resultados. Con el tiempo hemos podido diseñar la manera en la que se difunden resultados (ENLACE y PLANEA); utilizarlos para diseñar estrategias de intervención (Puebla y Sonora); y proponer nuevos esquemas de evaluación que puedan atender mejor las necesidades de información de los tomadores de decisiones de todos los niveles (INEE y PISA). 
                </p>
                <p>
                Desde el inicio de la prueba ENLACE en 2006, nos abocamos a conocer la manera en la que los resultados eran comunicados y utilizados en las escuelas y los hogares. De ahí que en 2008 la SEP nos solicitara diseñar e impartir talleres a funcionarios de todos los estados para conocer sus resultados y proponer acciones a partir de ellos. <strong> Esta experiencia nos permitió realizar un estudio en diversos estados para poder proponer, en 2009, nuevos reportes de resultados de esa prueba para docentes, padres de familia y comunidades escolares, mismos que fueron adoptados en 2010 por la SEP para todo el país</strong>. En el centro de la propuesta estaba la necesidad de adaptar la información a los intereses, posibilidades y niveles de comprensión de cada público, así como la necesidad de utilizar los resultados de evaluaciones para identificar a alumnos, docentes y escuelas con mayores necesidades de apoyo académico. En ello radica nuestra propuesta, que toma a la equidad como punto de partida para elevar la calidad.   
