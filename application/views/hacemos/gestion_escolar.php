            <h4 class="section-heading text-uppercase"><span>FORMACIÓN DE FUNCIONARIOS</span></h4>
            <p>En México se han realizado insuficientes esfuerzos para formar a las personas que laboran en las secretarías de educación federal y estatales. Si bien hay gente talentosa y comprometida en esas instancias, la vasta mayoría de ellos no tiene una formación académica directamente relacionada con la política educativa o con la operación del sistema educativo: son profesionistas que aprendieron a realizar sus tareas con la práctica. En adición a las implicaciones que ello tiene en la eficacia de la gestión educativa, <strong>la falta de formación pertinente en los funcionarios representa una enorme paradoja: el sector responsable de preparar a los cuadros de todos los ámbitos laborales del país no tiene esquemas para formar a sus propios cuadros</strong>. </p>
            <p>En 2008, la Fundación Proyecto Educativo AC y la Dra. Lucrecia Santibáñez promovimos la creación de la <strong>Beca Stanford – Claudio X. González – Conacyt, misma que desde entonces apoya cada año a estudiantes mexicanos para cursar programas de maestría en el área de política educativa en la Universidad de Stanford</strong>. La generosidad de los apoyos de la Fundación Olivier - Nomellini y del Ing. Claudio X. González Laporte, así como la estrategia diseñada por la universidad, permitieron que este apoyo haya sido establecido a perpetuidad. El principal requisito para quienes reciben la beca es trabajar a favor de la educación pública mexicana al concluir sus estudios. </p>
